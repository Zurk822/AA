    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        this.segundonumero=Float.parseFloat(this.rejilla.getText());
        
        switch(this.operador){
            case "+":this.rejilla.setText(sincero(this.primernumero+this.segundonumero));
            break;
            case "-":this.rejilla.setText(sincero(this.primernumero-this.segundonumero));
            break;
            case "*":this.rejilla.setText(sincero(this.primernumero*this.segundonumero));
            break;
            case "/":if(this.segundonumero==0){
                this.rejilla.setText("Syntax Error");
                break;
            }else{
                this.rejilla.setText(sincero(this.primernumero/this.segundonumero));
                break;
            }  
            case "^":
                
                this.rejilla.setText(sincero((float) Math.pow(this.primernumero, this.segundonumero)));
                break;
            
                
        }
    }//GEN-LAST:event_jButton18ActionPerformed

